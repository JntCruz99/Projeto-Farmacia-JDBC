    public void atualizar() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        CompraD pdao = new CompraD();
        for (Compra p : pdao.RelatorioCompras("CANCELADA")) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNomeCliente(),
                p.getStatus(),
                p.getForma_pagamento(),
                p.getQtd_itens()               
            });

        }
    }

