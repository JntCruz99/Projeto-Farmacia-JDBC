    public void atualizar(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ProdutoD pdao = new ProdutoD();
        for (Produto p : pdao.Produtos()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getFabricante(),
                p.getConcentracao(),
                p.getPreco(),
                p.getQtd()
                
            });

        }
    }
    public void buscartabela(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ProdutoD pdao = new ProdutoD();

        for (Produto p : pdao.BuscarProdutoDesc(desc)) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getFabricante(),
                p.getConcentracao(),
                p.getPreco(),
                p.getQtd()
            });

        }

    }

