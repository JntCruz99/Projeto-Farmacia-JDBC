    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {

            Produto p = new Produto();
            ProdutoD dao = new ProdutoD();

            p.setId(Integer.parseInt(jTextField2.getText()));
            p.setNome(jTextField3.getText());
            p.setFabricante(jTextField4.getText());
            p.setConcentracao(jTextField5.getText());          
            p.setPreco(Double.parseDouble(jTextField6.getText()));
            p.setQtd(Integer.parseInt(jTextField7.getText()));

            dao.UpdateProduto(p);
            
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            
            atualizar();
            DesativarTextfield();
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton3.setEnabled(false);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {

            Produto p = new Produto();
            ProdutoD dao = new ProdutoD();

            p.setId(Integer.parseInt(jTextField2.getText()));
            
            dao.DeleteProduto(p);

            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            
            atualizar();
            DesativarTextfield();
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton3.setEnabled(false);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


